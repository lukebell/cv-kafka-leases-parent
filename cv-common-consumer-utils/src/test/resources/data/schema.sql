DROP SCHEMA IF EXISTS INTRAWAY CASCADE;

CREATE SCHEMA INTRAWAY DEFAULT CHARACTER SET utf8;

CREATE TABLE INTRAWAY.CM_LEASES
(
   IPADDRESS varchar(64),
   MACADDRESS varchar(23),
   HOSTNAME varchar(100),
   FECHA date DEFAULT SYSDATE,
   MACRELAY varchar(23),
   IDLEASE decimal(22,0),
   DOCSIS_SUPPORT varchar(5),
   IDCLIENTE decimal(22,0),
   IDCMTS varchar(25),
   FECHA_REBOOT date,
   TIPO_PEDIDO decimal(2,0),
   IDDSP decimal(22,0),
   PRIMARY KEY (IDLEASE)
);

